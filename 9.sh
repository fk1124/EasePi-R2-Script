    case "$choice" in
      1)
        write_runtime_files
        if systemctl start routeros-chr.service; then
          ok "RouterOS 已启动。"
          info "在命令行输入 routeros 可进入 RouterOS 控制台。"
          info "在 RouterOS 控制台中按 Ctrl+] 可退出控制台。"
        else
          err "RouterOS 启动失败，请查看: journalctl -u routeros-chr.service -n 80 --no-pager"
        fi
        pause
        ;;
      2)
        if systemctl stop routeros-chr.service; then
          ok "RouterOS 已关闭。"
        else
          err "RouterOS 关闭失败，请查看 systemd 状态。"
        fi
        pause
        ;;
      3)
        write_runtime_files
        if systemctl restart routeros-chr.service; then
          ok "RouterOS 已重启。"
        else
          err "RouterOS 重启失败，请查看: journalctl -u routeros-chr.service -n 80 --no-pager"
        fi
        pause
        ;;
      4)
        write_runtime_files
        if systemctl enable routeros-chr.service; then
          ok "已设置开机自启动。"
        else
          err "设置开机自启动失败。"
        fi
        pause
        ;;
      5)
        if systemctl disable routeros-chr.service; then
          ok "已取消开机自启动。"
        else
          err "取消开机自启动失败。"
        fi
        pause
        ;;
      6)
        [ -x "$CONSOLE_CMD" ] || write_runtime_files
        "$CONSOLE_CMD"
        pause
        ;;
      7) uninstall_vm_service; pause ;;
      0) return ;;
      *) warn "无效选择"; pause ;;
    esac
  done
}

uninstall_installer(){
  warn "这只会卸载 routerosinstall 菜单命令本身。RouterOS 虚拟机服务请在菜单 5 中卸载。"
  if confirm "确认删除 $INSTALL_CMD" "n"; then
    rm -f "$INSTALL_CMD"
    ok "routerosinstall 命令已删除。"
    exit 0
  fi
}

menu_exit_or_uninstall(){
  while true; do
    print_title
    printf '%s\n' "1. 退出脚本"
    printf '%s\n' "2. 卸载 routerosinstall 命令"
    printf '%s\n' "0. 返回主菜单"
    printf '%s' "请选择: "
    local choice
    IFS= read -r choice || true
    case "$choice" in
      1) exit 0 ;;
      2) uninstall_installer ;;
      0) return ;;
      *) warn "无效选择"; pause ;;
    esac
  done
}

