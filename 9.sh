set -uo pipefail

VERSION="2026-05-19-routeros-chr-installer-r7"

INSTALL_CMD="/usr/local/sbin/routerosinstall"
CONSOLE_CMD="/usr/local/sbin/routeros"
CONFIG_DIR="/etc/routerosinstall"
CONFIG_FILE="$CONFIG_DIR/config.env"
PRESET_RSC="$CONFIG_DIR/routeros-preset.rsc"
LIB_DIR="/usr/local/lib/routerosinstall"
HOSTNET_SCRIPT="$LIB_DIR/hostnet.sh"
START_SCRIPT="$LIB_DIR/start-vm.sh"
SERVICE_FILE="/etc/systemd/system/routeros-chr.service"
ROLLBACK_SCRIPT="$CONFIG_DIR/last-hostnet-rollback.sh"
NM_UNMANAGED_CONF="/etc/NetworkManager/conf.d/99-routerosinstall-unmanaged.conf"
NETWORKD_PREFIX="/etc/systemd/network/00-routerosinstall"
NETWORKD_LEGACY_PREFIX="/etc/systemd/network/80-routerosinstall"

RED=$'\033[31m'
GREEN=$'\033[32m'
YELLOW=$'\033[33m'
BLUE=$'\033[34m'
BOLD=$'\033[1m'
RESET=$'\033[0m'

ok(){ printf '%s\n' "${GREEN}[完成]${RESET} $*" >&2; }
info(){ printf '%s\n' "${BLUE}[信息]${RESET} $*" >&2; }
warn(){ printf '%s\n' "${YELLOW}[提醒]${RESET} $*" >&2; }
err(){ printf '%s\n' "${RED}[错误]${RESET} $*" >&2; }
has_cmd(){ command -v "$1" >/dev/null 2>&1; }

pause(){
  printf '%s' "按回车继续..." >&2
  IFS= read -r _ || true
}

need_root(){
  if [ "${EUID:-$(id -u)}" -ne 0 ]; then
    err "请使用 root 执行：sudo bash 9.sh 或 sudo routerosinstall"
    exit 1
  fi
}

read_default(){
  local prompt="$1" def="${2:-}" val
  if [ -n "$def" ]; then
    printf '%s' "$prompt [$def]: " >&2
  else
    printf '%s' "$prompt: " >&2
  fi
  IFS= read -r val || true
  printf '%s' "${val:-$def}"
}

confirm(){
  local prompt="$1" def="${2:-y}" val hint
  if [ "$def" = "y" ]; then
    hint="Y/n"
  else
    hint="y/N"
  fi
  printf '%s' "$prompt [$hint]: " >&2
  IFS= read -r val || true
  val="${val:-$def}"
  case "${val,,}" in
    y|yes|1|true|ok|是|好|确认) return 0 ;;
    *) return 1 ;;
  esac
}

space_to_continue(){
  local key
  printf '%s' "按空格继续执行，按其他任意键取消: " >&2
  IFS= read -rsn1 key || true
  printf '\n' >&2
  [ "$key" = " " ]
}

print_title(){
  clear 2>/dev/null || true
  printf '%s\n' "${BOLD}RouterOS CHR KVM 安装与管理脚本${RESET}"
  printf '%s\n' "版本: $VERSION"
  printf '%s\n\n' "配置目录: $CONFIG_DIR"
}

quote_env(){
  printf '%q' "$1"
}

atomic_write(){
  local target="$1" mode="${2:-0644}" dir tmp
  dir="$(dirname "$target")"
  mkdir -p "$dir"
  tmp="$(mktemp "$dir/.routerosinstall.XXXXXX")" || return 1
  if ! cat > "$tmp"; then
    rm -f "$tmp"
    return 1
  fi
  chmod "$mode" "$tmp" 2>/dev/null || true
  mv -f "$tmp" "$target"
}

is_uint(){
  [[ "${1:-}" =~ ^[0-9]+$ ]]
}

is_ifname(){
  [[ "${1:-}" =~ ^[A-Za-z0-9_.:-]{1,15}$ ]]
}

is_ros_ifname(){
  [[ "${1:-}" =~ ^[A-Za-z0-9_.:-]{1,63}$ ]]
}

is_mac(){
  [[ "${1:-}" =~ ^([0-9A-Fa-f]{2}:){5}[0-9A-Fa-f]{2}$ ]]
}

is_ipv4(){
  local IFS=. a b c d extra oct
  [[ "${1:-}" =~ ^[0-9]+(\.[0-9]+){3}$ ]] || return 1
  read -r a b c d extra <<< "$1"
  [ -z "${extra:-}" ] || return 1
  for oct in "$a" "$b" "$c" "$d"; do
    is_uint "$oct" || return 1
    [ "$((10#$oct))" -ge 0 ] && [ "$((10#$oct))" -le 255 ] || return 1
  done
}

is_prefix(){
  is_uint "${1:-}" && [ "$1" -ge 0 ] && [ "$1" -le 32 ]
}

is_size_value(){
  [[ "${1:-}" =~ ^[0-9]+([KkMmGgTtPp])?$ ]]
}

trim(){
  local s="$1"
  s="${s#"${s%%[![:space:]]*}"}"
  s="${s%"${s##*[![:space:]]}"}"
  printf '%s' "$s"
}

strip_spaces(){
  local s="$1"
  s="${s//[[:space:]]/}"
  printf '%s' "$s"
}

normalize_uint(){
  local label="$1" value="$2" def="$3" min="$4" max="${5:-}"
  if ! is_uint "$value"; then
    warn "$label 不是有效数字，已恢复为 $def"
    value="$def"
  fi
  if [ "$value" -lt "$min" ]; then
    warn "$label 不能小于 $min，已调整。"
    value="$min"
  fi
  if [ -n "$max" ] && [ "$value" -gt "$max" ]; then
    warn "$label 不能大于 $max，已调整。"
    value="$max"
  fi
  printf '%s' "$value"
}

validate_name_list(){
  local label="$1" checker="$2" list="$3" item ok=0
  for item in $list; do
    if ! "$checker" "$item"; then
      err "$label 包含非法名称: $item"
      ok=1
    fi
  done
  return "$ok"
}

management_ifaces(){
  {
    ip route show default 2>/dev/null | awk '{for (i=1; i<=NF; i++) if ($i=="dev") print $(i+1)}'
    if [ -n "${SSH_CONNECTION:-}" ]; then
      local remote="${SSH_CONNECTION%% *}"
      ip route get "$remote" 2>/dev/null | awk '{for (i=1; i<=NF; i++) if ($i=="dev") print $(i+1)}'
    fi
  } | awk 'NF && !seen[$0]++'
}

iface_is_management(){
  local iface="$1" mgmt
  while IFS= read -r mgmt; do
    [ "$iface" = "$mgmt" ] && return 0
  done < <(management_ifaces)
  return 1
}

iface_has_host_network(){
  local iface="$1"
  ip -o addr show dev "$iface" 2>/dev/null | grep -q . && return 0
  ip route show dev "$iface" 2>/dev/null | grep -q . && return 0
  return 1
}

write_host_rollback_script(){
  local script="${1:-$ROLLBACK_SCRIPT}" iface addr route
  mkdir -p "$CONFIG_DIR"
  {
    printf '%s\n' '#!/usr/bin/env bash'
    printf '%s\n' 'set -u'
    printf '# Generated by routerosinstall at %s\n' "$(date -Is 2>/dev/null || date)"
    for iface in $VM_IFACES; do
      [ -n "$iface" ] || continue
      [ "$iface" = "none" ] && continue
      ip link show "$iface" >/dev/null 2>&1 || continue
      printf 'ip link set %q nomaster 2>/dev/null || true\n' "$iface"
      printf 'ip link set %q up 2>/dev/null || true\n' "$iface"
      while IFS= read -r addr; do
        [ -n "$addr" ] || continue
        printf 'ip addr add %q dev %q 2>/dev/null || true\n' "$addr" "$iface"
      done < <(ip -o addr show dev "$iface" 2>/dev/null | awk '{print $4}')
      while IFS= read -r route; do
        [ -n "$route" ] || continue
        printf 'ip route replace %s 2>/dev/null || true\n' "$route"
      done < <(ip route show dev "$iface" 2>/dev/null)
    done
  } | atomic_write "$script" 0700
}

confirm_host_iface_takeover(){
  local dangerous=() iface answer
  for iface in $VM_IFACES; do
    [ -n "$iface" ] || continue
    [ "$iface" = "none" ] && continue
    if ! is_ifname "$iface"; then
      err "宿主机网口名称非法: $iface"
      return 1
    fi
    if ! ip link show "$iface" >/dev/null 2>&1; then
      warn "宿主机网口不存在，启动时会跳过: $iface"
      continue
    fi
    if iface_is_management "$iface" || iface_has_host_network "$iface"; then
      dangerous+=("$iface")
    fi
  done

  if [ "${#dangerous[@]}" -eq 0 ]; then
    ALLOW_DANGEROUS_IFACES=""
    return 0
  fi

  printf '\n'
  warn "以下网口当前带有宿主机地址/路由，或看起来是管理入口: ${dangerous[*]}"
  warn "RouterOS 启动时会清空这些网口 IP 并加入 br-rosX，SSH 可能断开。"
  write_host_rollback_script "$ROLLBACK_SCRIPT" || true
  info "已生成尽力回滚脚本: $ROLLBACK_SCRIPT"
  answer="$(read_default "如果确认仍要接管，请输入 YES" "")"
  if [ "$answer" != "YES" ]; then
    err "已取消网口接管配置。"
    return 1
  fi
  ALLOW_DANGEROUS_IFACES="${dangerous[*]}"
}

qemu_img_format_probe(){
  local path="$1" timeout_s="${2:-3}" actual
  has_cmd qemu-img || return 1
  has_cmd timeout || return 1
  [ -f "$path" ] || return 1

  if ! actual="$(timeout "$timeout_s" qemu-img info "$path" 2>/dev/null | awk -F': ' '/^file format:/ {print $2; exit}')"; then
    return 1
  fi
  case "$actual" in
    raw|qcow2|vdi|vmdk) printf '%s' "$actual"; return 0 ;;
  esac
  return 1
}

detect_disk_format(){
  local path="$1" guessed actual
  guessed="$(guess_disk_format "$path")"
  case "${path,,}" in
    *.img|*.raw) printf '%s' "$guessed"; return 0 ;;
  esac
  if actual="$(qemu_img_format_probe "$path" 3)"; then
    printf '%s' "$actual"
    return 0
  fi
  printf '%s' "$guessed"
}

validate_vm_config(){
  local ok_flag=0 count_if count_br count_tap count_mac item cpu_count
  VM_CPUS="$(normalize_uint "vCPU 数量" "$VM_CPUS" "$(nproc 2>/dev/null || echo 4)" 1 256)"
  VM_MEMORY_MB="$(normalize_uint "内存 MB" "$VM_MEMORY_MB" "1024" 128 262144)"
  NET_QUEUES="$(normalize_uint "virtio-net 队列数" "$NET_QUEUES" "1" 1 16)"
  cpu_count="$(nproc 2>/dev/null || echo 0)"
  if is_uint "$cpu_count" && [ "$cpu_count" -gt 0 ] && [ "$VM_CPUS" -gt "$cpu_count" ]; then
    warn "vCPU 数量大于宿主机逻辑 CPU 数，QEMU 可以超分，但不建议生产长期这样跑。"
  fi
  case "${DISK_FORMAT,,}" in
    raw|qcow2|vdi|vmdk) DISK_FORMAT="${DISK_FORMAT,,}" ;;
    *) warn "未知镜像格式 $DISK_FORMAT，已按 raw 处理。"; DISK_FORMAT="raw" ;;
  esac
  case "${HOST_NET_MODE:-}" in
    runtime|networkd|nm-unmanaged|both) ;;
    *)
      if [ "${PERSIST_HOST_NET:-1}" = "1" ]; then
        HOST_NET_MODE="networkd"
      else
        HOST_NET_MODE="runtime"
      fi
      ;;
  esac
  case "${HOST_NET_MODE:-}" in
    runtime) PERSIST_HOST_NET="0" ;;
    *) PERSIST_HOST_NET="1" ;;
  esac

  count_if="$(wc -w <<< "${VM_IFACES:-}")"
  count_br="$(wc -w <<< "${VM_BRIDGES:-}")"
  count_tap="$(wc -w <<< "${VM_TAPS:-}")"
  count_mac="$(wc -w <<< "${VM_MACS:-}")"
  if [ "$count_if" -lt 1 ] || [ "$count_if" -ne "$count_br" ] || [ "$count_if" -ne "$count_tap" ] || [ "$count_if" -ne "$count_mac" ]; then
    err "VM_IFACES/VM_BRIDGES/VM_TAPS/VM_MACS 数量不一致。"
    ok_flag=1
  fi
  for item in $VM_IFACES; do
    [ "$item" = "none" ] && continue
    is_ifname "$item" || { err "宿主机网口名称非法: $item"; ok_flag=1; }
  done
  validate_name_list "桥名称" is_ifname "$VM_BRIDGES" || ok_flag=1
  validate_name_list "tap 名称" is_ifname "$VM_TAPS" || ok_flag=1
  for item in $VM_MACS; do
    is_mac "$item" || { err "MAC 地址非法: $item"; ok_flag=1; }
  done
  [ "$ok_flag" -eq 0 ]
}

validate_routeros_config(){
  local ok_flag=0 dns dns_trim start_int end_int lan_net start_net end_net _dns_list
  [ -n "${ROS_IDENTITY:-}" ] || { err "RouterOS identity 不能为空。"; ok_flag=1; }
  is_ros_ifname "$ROS_WAN_IFACE" || { err "WAN 口名称非法: $ROS_WAN_IFACE"; ok_flag=1; }
  is_ros_ifname "$ROS_LAN_BRIDGE" || { err "LAN bridge 名称非法: $ROS_LAN_BRIDGE"; ok_flag=1; }
  validate_name_list "LAN 口列表" is_ros_ifname "$ROS_LAN_PORTS" || ok_flag=1
  is_ipv4 "$ROS_LAN_IP" || { err "LAN IP 非法: $ROS_LAN_IP"; ok_flag=1; }
  is_prefix "$ROS_LAN_PREFIX" || { err "LAN 掩码前缀非法: $ROS_LAN_PREFIX"; ok_flag=1; }
  is_ipv4 "$ROS_DHCP_START" || { err "DHCP 起始地址非法: $ROS_DHCP_START"; ok_flag=1; }
  is_ipv4 "$ROS_DHCP_END" || { err "DHCP 结束地址非法: $ROS_DHCP_END"; ok_flag=1; }
  IFS=',' read -r -a _dns_list <<< "$ROS_DNS_SERVERS"
  for dns in "${_dns_list[@]}"; do
    dns_trim="$(trim "$dns")"
    is_ipv4 "$dns_trim" || { err "DNS 地址非法: $dns_trim"; ok_flag=1; }
  done
  if [ "$ok_flag" -eq 0 ]; then
    start_int="$(ip_to_int "$ROS_DHCP_START")"
    end_int="$(ip_to_int "$ROS_DHCP_END")"
    if [ "$start_int" -gt "$end_int" ]; then
      err "DHCP 起始地址不能大于结束地址。"
      ok_flag=1
    fi
    lan_net="$(cidr_network "$ROS_LAN_IP" "$ROS_LAN_PREFIX")"
    start_net="$(cidr_network "$ROS_DHCP_START" "$ROS_LAN_PREFIX")"
    end_net="$(cidr_network "$ROS_DHCP_END" "$ROS_LAN_PREFIX")"
    if [ "$lan_net" != "$start_net" ] || [ "$lan_net" != "$end_net" ]; then
      warn "DHCP 地址池不在 LAN 网段 $lan_net/$ROS_LAN_PREFIX 内。"
    fi
  fi
  [ "$ok_flag" -eq 0 ]
}

