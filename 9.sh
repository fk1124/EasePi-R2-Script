  done
  if [ -n "$unmanaged" ]; then
    atomic_write "$NM_UNMANAGED_CONF" 0644 <<EOF_NM
[keyfile]
unmanaged-devices=$unmanaged
EOF_NM
  fi
}

reload_persistent_network_managers(){
  local mode="${HOST_NET_MODE:-networkd}"
  local iface br

  case "$mode" in
    networkd|both|runtime)
      if has_cmd networkctl; then
        networkctl reload 2>/dev/null || true
        for br in $VM_BRIDGES; do
          [ -n "$br" ] || continue
          networkctl reconfigure "$br" 2>/dev/null || true
        done
        for iface in $VM_IFACES; do
          [ -n "$iface" ] || continue
          [ "$iface" = "none" ] && continue
          networkctl reconfigure "$iface" 2>/dev/null || true
        done
      elif has_cmd systemctl; then
        systemctl reload systemd-networkd 2>/dev/null || true
      fi
      ;;
  esac

  case "$mode" in
    nm-unmanaged|both|runtime)
      if has_cmd systemctl; then
        systemctl reload NetworkManager 2>/dev/null || systemctl restart NetworkManager 2>/dev/null || true
      fi
      ;;
  esac
}

write_persistent_network_files(){
  case "${HOST_NET_MODE:-networkd}" in
    runtime)
      remove_persistent_network_files
      ;;
    networkd)
      rm -f "$NM_UNMANAGED_CONF" 2>/dev/null || true
      write_networkd_files
      ;;
    nm-unmanaged)
      rm -f "$NETWORKD_PREFIX"-*.netdev "$NETWORKD_PREFIX"-*.network 2>/dev/null || true
      rm -f "$NETWORKD_LEGACY_PREFIX"-*.netdev "$NETWORKD_LEGACY_PREFIX"-*.network 2>/dev/null || true
      write_nm_unmanaged_file
      ;;
    both)
      write_networkd_files
      write_nm_unmanaged_file
      ;;
    *)
      warn "未知持久化模式 ${HOST_NET_MODE:-空}，按 runtime 处理。"
      HOST_NET_MODE="runtime"
      PERSIST_HOST_NET="0"
      remove_persistent_network_files
      ;;
  esac

  reload_persistent_network_managers
}

