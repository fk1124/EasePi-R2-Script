iface_has_host_network(){
  local iface="$1"
  ip -o addr show dev "$iface" 2>/dev/null | grep -q . && return 0
  ip route show dev "$iface" 2>/dev/null | grep -q . && return 0
  return 1
}

management_ifaces(){
  {
    ip route show default 2>/dev/null | awk '{for (i=1; i<=NF; i++) if ($i=="dev") print $(i+1)}'
    if [ -n "${SSH_CONNECTION:-}" ]; then
      local remote="${SSH_CONNECTION%% *}"
      ip route get "$remote" 2>/dev/null | awk '{for (i=1; i<=NF; i++) if ($i=="dev") print $(i+1)}'
    fi
  } | awk 'NF && !seen[$0]++'
}

iface_is_management(){
  local iface="$1" mgmt
  while IFS= read -r mgmt; do
    [ "$iface" = "$mgmt" ] && return 0
  done < <(management_ifaces)
  return 1
}

write_rollback_script(){
  local iface addr route tmp
  mkdir -p "$(dirname "$ROLLBACK_SCRIPT")"
  tmp="$(mktemp "$(dirname "$ROLLBACK_SCRIPT")/.rollback.XXXXXX")" || return 1
  {
    printf '%s\n' '#!/usr/bin/env bash'
    printf '%s\n' 'set -u'
    printf '# Generated by routerosinstall hostnet at %s\n' "$(date -Is 2>/dev/null || date)"
    for iface in "${ifaces[@]}"; do
      [ -n "$iface" ] || continue
      [ "$iface" = "none" ] && continue
      ip link show "$iface" >/dev/null 2>&1 || continue
      printf 'ip link set %q nomaster 2>/dev/null || true\n' "$iface"
      printf 'ip link set %q up 2>/dev/null || true\n' "$iface"
      while IFS= read -r addr; do
        [ -n "$addr" ] || continue
        printf 'ip addr add %q dev %q 2>/dev/null || true\n' "$addr" "$iface"
      done < <(ip -o addr show dev "$iface" 2>/dev/null | awk '{print $4}')
      while IFS= read -r route; do
        [ -n "$route" ] || continue
        printf 'ip route replace %s 2>/dev/null || true\n' "$route"
      done < <(ip route show dev "$iface" 2>/dev/null)
    done
  } > "$tmp"
  chmod 0700 "$tmp" 2>/dev/null || true
  mv -f "$tmp" "$ROLLBACK_SCRIPT"
}

preflight_up(){
  local i br tap iface bad=0
  is_uint "$queues" || queues=1
  [ "$queues" -ge 1 ] || queues=1
  if [ "${#bridges[@]}" -eq 0 ]; then
    echo "routerosinstall: no bridge configured" >&2
    return 1
  fi
  for i in "${!bridges[@]}"; do
    br="${bridges[$i]}"
    tap="${taps[$i]:-}"
    iface="${ifaces[$i]:-none}"
    if ! is_ifname "$br"; then
      echo "routerosinstall: invalid bridge name: $br" >&2
      bad=1
    fi
    if [ -n "$tap" ] && ! is_ifname "$tap"; then
      echo "routerosinstall: invalid tap name: $tap" >&2
      bad=1
    fi
    if [ -n "$iface" ] && [ "$iface" != "none" ]; then
      if ! is_ifname "$iface"; then
        echo "routerosinstall: invalid host iface name: $iface" >&2
        bad=1
      elif ! ip link show "$iface" >/dev/null 2>&1; then
        echo "routerosinstall: host iface not found: $iface" >&2
        bad=1
      elif { iface_is_management "$iface" || iface_has_host_network "$iface"; } && ! is_allowed_danger_iface "$iface"; then
        echo "routerosinstall: refusing to take over active/management iface without prior YES confirmation: $iface" >&2
        bad=1
      fi
    fi
  done
  [ "$bad" -eq 0 ]
}

create_tap(){
  local tap="$1"
  if ip link show "$tap" >/dev/null 2>&1; then
    printf 'tap %s existing\n' "$tap" >> "$STATE_FILE"
    return 0
  fi
  if [ "${queues:-1}" -gt 1 ] 2>/dev/null; then
    ip tuntap add dev "$tap" mode tap multi_queue 2>/dev/null || ip tuntap add dev "$tap" mode tap
  else
    ip tuntap add dev "$tap" mode tap
  fi
  printf 'tap %s created\n' "$tap" >> "$STATE_FILE"
}

bridge_created_by_us(){
  local br="$1"
  grep -qxF "bridge $br created" "$STATE_FILE" 2>/dev/null
}

tap_created_by_us(){
  local tap="$1"
  grep -qxF "tap $tap created" "$STATE_FILE" 2>/dev/null
}

case "$ACTION" in
  up)
    preflight_up
    : > "$STATE_FILE"
    write_rollback_script || true
    modprobe tun 2>/dev/null || true
    modprobe vhost_net 2>/dev/null || true
    for i in "${!bridges[@]}"; do
      br="${bridges[$i]}"
      tap="${taps[$i]:-}"
      iface="${ifaces[$i]:-none}"
      if ip link show "$br" >/dev/null 2>&1; then
        printf 'bridge %s existing\n' "$br" >> "$STATE_FILE"
      else
        ip link add name "$br" type bridge
        printf 'bridge %s created\n' "$br" >> "$STATE_FILE"
      fi
      ip link set "$br" up
      if [ -n "$tap" ]; then
        create_tap "$tap"
        ip link set "$tap" master "$br"
        ip link set "$tap" up
      fi
      if [ -n "$iface" ] && [ "$iface" != "none" ]; then
        if ip link show "$iface" >/dev/null 2>&1; then
          ip addr flush dev "$iface" 2>/dev/null || true
          ip link set "$iface" nomaster 2>/dev/null || true
          ip link set "$iface" up 2>/dev/null || true
          if ! ip link set "$iface" master "$br" 2>/dev/null; then
            echo "routerosinstall: failed to attach $iface to $br" >&2
            exit 1
          fi
          current_master="$(basename "$(readlink -f "/sys/class/net/$iface/master" 2>/dev/null)" 2>/dev/null || true)"
          if [ "$current_master" != "$br" ]; then
            echo "routerosinstall: $iface is still attached to ${current_master:-none}, expected $br" >&2
            exit 1
          fi
        else
          echo "routerosinstall: host iface $iface not found" >&2
        fi
      fi
    done
    ;;
  down)
    for i in "${!bridges[@]}"; do
      br="${bridges[$i]}"
      tap="${taps[$i]:-}"
      iface="${ifaces[$i]:-none}"
      if [ -n "$iface" ] && [ "$iface" != "none" ] && ip link show "$iface" >/dev/null 2>&1; then
        ip link set "$iface" nomaster 2>/dev/null || true
      fi
      if [ -n "$tap" ] && ip link show "$tap" >/dev/null 2>&1; then
        ip link set "$tap" down 2>/dev/null || true
        if tap_created_by_us "$tap"; then
          ip link delete "$tap" 2>/dev/null || true
        fi
      fi
      if ip link show "$br" >/dev/null 2>&1; then
        if bridge_created_by_us "$br"; then
          ip link set "$br" down 2>/dev/null || true
          ip link delete "$br" type bridge 2>/dev/null || true
        fi
      fi
    done
    rm -f "$STATE_FILE" 2>/dev/null || true
    ;;
  *)
    echo "usage: $0 up|down" >&2
    exit 2
    ;;
esac
EOF_HOSTNET

