qemu_img_format_probe(){
  local path="$1" timeout_s="${2:-3}" actual
  command -v qemu-img >/dev/null 2>&1 || return 1
  command -v timeout >/dev/null 2>&1 || return 1
  [ -f "$path" ] || return 1

  if ! actual="$(timeout "$timeout_s" qemu-img info "$path" 2>/dev/null | awk -F': ' '/^file format:/ {print $2; exit}')"; then
    return 1
  fi
  case "$actual" in
    raw|qcow2|vdi|vmdk) printf '%s' "$actual"; return 0 ;;
  esac
  return 1
}

find_aarch64_efi_firmware(){
  local fw
  for fw in \
    /usr/share/qemu-efi-aarch64/QEMU_EFI.fd \
    /usr/share/AAVMF/AAVMF_CODE.fd \
    /usr/share/AAVMF/AAVMF_CODE.no-secboot.fd; do
    [ -r "$fw" ] && { printf '%s\n' "$fw"; return 0; }
  done
  return 1
}

preflight_start(){
  local tap mac actual arch bad=0
  if ! command -v "$QEMU_BIN" >/dev/null 2>&1; then
    echo "routerosinstall: QEMU not found: $QEMU_BIN" >&2
    return 1
  fi
  if [ ! -f "$IMAGE_PATH" ]; then
    echo "routerosinstall: image not found: $IMAGE_PATH" >&2
    return 1
  fi
  if ! is_uint "$VM_CPUS" || [ "$VM_CPUS" -lt 1 ]; then
    echo "routerosinstall: invalid VM_CPUS: $VM_CPUS" >&2
    bad=1
  fi
  if ! is_uint "$VM_MEMORY_MB" || [ "$VM_MEMORY_MB" -lt 128 ]; then
    echo "routerosinstall: invalid VM_MEMORY_MB: $VM_MEMORY_MB" >&2
    bad=1
  fi
  if ! is_uint "$NET_QUEUES" || [ "$NET_QUEUES" -lt 1 ]; then
    echo "routerosinstall: invalid NET_QUEUES: $NET_QUEUES" >&2
    bad=1
  fi
  case "${DISK_FORMAT,,}" in
    raw|qcow2|vdi|vmdk) ;;
    *) echo "routerosinstall: unsupported DISK_FORMAT: $DISK_FORMAT" >&2; bad=1 ;;
  esac
  actual=""
  case "${IMAGE_PATH,,}" in
    *.img|*.raw) ;;
    *) actual="$(qemu_img_format_probe "$IMAGE_PATH" 3 || true)" ;;
  esac
  if [ -n "$actual" ] && [ "$actual" != "$DISK_FORMAT" ]; then
    echo "routerosinstall: configured DISK_FORMAT=$DISK_FORMAT, qemu-img reports $actual; using reported format" >&2
    DISK_FORMAT="$actual"
  fi
  for tap in "${taps[@]}"; do
    if ! is_ifname "$tap"; then
      echo "routerosinstall: invalid tap name: $tap" >&2
      bad=1
    elif ! ip link show "$tap" >/dev/null 2>&1; then
      echo "routerosinstall: tap is missing, hostnet pre-start may have failed: $tap" >&2
      bad=1
    fi
  done
  for mac in "${macs[@]}"; do
    if ! is_mac "$mac"; then
      echo "routerosinstall: invalid MAC address: $mac" >&2
      bad=1
    fi
  done
  arch="$(uname -m)"
  if { [ "$arch" = "aarch64" ] || [ "$arch" = "arm64" ]; } && ! find_aarch64_efi_firmware >/dev/null; then
    echo "routerosinstall: missing AArch64 UEFI firmware; install qemu-efi-aarch64" >&2
    bad=1
  fi
  [ "$bad" -eq 0 ]
}

mkdir -p "$(dirname "$SERIAL_SOCK")"
rm -f "$SERIAL_SOCK" "$MONITOR_SOCK" "$PID_FILE"

preflight_start

arch="$(uname -m)"
efi_fw=""
args=(-name routeros-chr)
if { [ "$arch" = "aarch64" ] || [ "$arch" = "arm64" ]; } && [ -e /dev/kvm ]; then
