  if [ "$remove_net" = "y" ]; then
    systemctl restart systemd-networkd 2>/dev/null || true
    systemctl reload NetworkManager 2>/dev/null || true
  fi
  ok "RouterOS 虚拟机服务已卸载。镜像文件不会自动删除，请手动确认后处理。"
}

