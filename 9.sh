RequiredForOnline=no
ActivationPolicy=up

[Network]
Bridge=$br
ConfigureWithoutCarrier=yes
IgnoreCarrierLoss=yes
LinkLocalAddressing=no
IPv6AcceptRA=no
EOF_PORTNET
