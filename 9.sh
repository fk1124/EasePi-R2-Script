guess_disk_format(){
  case "${1,,}" in
    *.qcow2) printf '%s' "qcow2" ;;
    *.vdi) printf '%s' "vdi" ;;
    *.vmdk) printf '%s' "vmdk" ;;
    *) printf '%s' "raw" ;;
  esac
}

disk_ext_for_format(){
  case "${1,,}" in
    qcow2) printf '%s' "qcow2" ;;
    vdi) printf '%s' "vdi" ;;
    vmdk) printf '%s' "vmdk" ;;
    *) printf '%s' "img" ;;
  esac
}

extract_zip_image(){
  local zipfile="$1" entry="$2" entry_format out tmp raw_tmp ext
  entry_format="$(guess_disk_format "$entry")"

  if [ "$entry_format" != "raw" ] && has_cmd qemu-img; then
    out="/root/routeros.img"
    if [ -f "$out" ] && ! confirm "$out 已存在，是否覆盖" "n"; then
      warn "已取消解压转换。"
      return 1
    fi
    tmp="$(mktemp "/root/.routerosinstall-${entry_format}.XXXXXX")" || return 1
    raw_tmp="$(mktemp "/root/.routerosinstall-raw.XXXXXX")" || { rm -f "$tmp"; return 1; }
    info "正在从 $zipfile 解压 $entry"
    if ! unzip -p "$zipfile" "$entry" > "$tmp"; then
      rm -f "$tmp" "$raw_tmp"
      return 1
    fi
    info "正在把 $entry_format 转换为 raw: $out"
    rm -f "$raw_tmp"
    if ! qemu-img convert -f "$entry_format" -O raw "$tmp" "$raw_tmp"; then
      rm -f "$tmp" "$raw_tmp"
      return 1
    fi
    chmod 0600 "$raw_tmp" 2>/dev/null || true
    mv -f "$raw_tmp" "$out"
    rm -f "$tmp"
    IMAGE_PATH="$out"
    DISK_FORMAT="raw"
    return 0
  fi

  ext="$(disk_ext_for_format "$entry_format")"
  out="/root/routeros.$ext"
  if [ -f "$out" ] && ! confirm "$out 已存在，是否覆盖" "n"; then
    warn "已取消解压。"
    return 1
  fi
  tmp="$(mktemp "/root/.routerosinstall-image.XXXXXX")" || return 1
  info "正在从 $zipfile 解压 $entry 到 $out"
  if ! unzip -p "$zipfile" "$entry" > "$tmp"; then
    rm -f "$tmp"
    return 1
  fi
  chmod 0600 "$tmp" 2>/dev/null || true
  mv -f "$tmp" "$out"
  IMAGE_PATH="$out"
  DISK_FORMAT="$(detect_disk_format "$IMAGE_PATH")"
}

choose_image_from_root(){
  local default="/root/routeros.img"
  local -a files
  local i sel chosen entry
  files=()

  if [ -f "$default" ]; then
    ok "检测到 $default"
    IMAGE_PATH="$default"
    DISK_FORMAT="$(detect_disk_format "$IMAGE_PATH")"
    return 0
  fi

  while IFS= read -r f; do
    files+=("$f")
  done < <(find /root -maxdepth 1 -type f \( -iname '*.img' -o -iname '*.raw' -o -iname '*.qcow2' -o -iname '*.vdi' -o -iname '*.vmdk' -o -iname '*.img.zip' -o -iname '*.vdi.zip' -o -iname '*.zip' \) 2>/dev/null | sort)

  if [ "${#files[@]}" -eq 0 ]; then
    warn "/root 下没有找到 routeros.img 或其他 img/zip 镜像。"
    IMAGE_PATH="$(read_default "请输入 RouterOS 镜像完整路径" "$IMAGE_PATH")"
    DISK_FORMAT="$(guess_disk_format "$IMAGE_PATH")"
    return 0
  fi

  printf '%s\n' "没有找到 /root/routeros.img，但发现以下镜像候选:"
  for i in "${!files[@]}"; do
    printf '  %s. %s\n' "$((i+1))" "${files[$i]}"
  done
  printf '%s' "选择要使用的镜像编号，直接回车手动输入路径: "
  IFS= read -r sel || true
  if [ -z "$sel" ]; then
    IMAGE_PATH="$(read_default "请输入 RouterOS 镜像完整路径" "$IMAGE_PATH")"
    DISK_FORMAT="$(detect_disk_format "$IMAGE_PATH")"
    return 0
  fi
  if ! [[ "$sel" =~ ^[0-9]+$ ]] || [ "$sel" -lt 1 ] || [ "$sel" -gt "${#files[@]}" ]; then
    warn "选择无效，保持当前路径: $IMAGE_PATH"
    return 1
  fi

  chosen="${files[$((sel-1))]}"
  case "${chosen,,}" in
    *.zip)
      if ! has_cmd unzip; then
        err "缺少 unzip，先执行菜单 1 安装依赖。"
        return 1
      fi
      entry="$(unzip -Z1 "$chosen" 2>/dev/null | grep -Ei '\.(img|raw|qcow2|vdi|vmdk)$' | head -n1 || true)"
      if [ -z "$entry" ]; then
        err "压缩包内没有找到 img/raw/qcow2/vdi/vmdk 文件。"
        return 1
      fi
      extract_zip_image "$chosen" "$entry"
      ;;
    *)
      IMAGE_PATH="$chosen"
      DISK_FORMAT="$(detect_disk_format "$IMAGE_PATH")"
      ;;
  esac
}

choose_host_ifaces(){
  local -a phys chosen
  local count def iface i macs bridges taps input duplicate
  phys=()
  chosen=()
  while IFS= read -r iface; do
    phys+=("$iface")
  done < <(physical_ifaces)

  printf '\n%s\n' "可选物理网口:"
  if [ "${#phys[@]}" -eq 0 ]; then
    warn "没有自动发现物理网口，你仍可手动输入。"
  else
    for iface in "${phys[@]}"; do
      show_iface_usage "$iface"
    done
  fi

  count="$(read_default "给 RouterOS 配置几个 virtio-net 网口" "3")"
  count="$(normalize_uint "virtio-net 网口数量" "$count" "3" 1 16)"

  for ((i=1; i<=count; i++)); do
    case "$i" in
      1) def="eth1" ;;
      2) def="eth2" ;;
      3) def="eth3" ;;
      *) def="" ;;
    esac
    while true; do
      input="$(read_default "第 $i 个 virtio-net 绑定的宿主机物理网口，输入 none 表示只建内部桥" "$def")"
      input="$(trim "$input")"
      [ -n "$input" ] || input="none"
      if [ "$input" != "none" ] && ! is_ifname "$input"; then
        warn "网口名称非法，请重新输入。"
        continue
      fi
      duplicate=0
      for iface in "${chosen[@]}"; do
        if [ "$input" != "none" ] && [ "$iface" = "$input" ]; then
          duplicate=1
          break
        fi
      done
      if [ "$duplicate" -eq 1 ]; then
        warn "同一个宿主机物理网口不能重复绑定。"
        continue
      fi
      chosen+=("$input")
      break
    done
  done

  VM_IFACES="${chosen[*]}"
  bridges=()
  taps=()
  macs=()
  for ((i=1; i<=count; i++)); do
    bridges+=("br-ros$i")
    taps+=("tap-ros$i")
    macs+=("52:54:00:21:00:$(printf '%02x' "$i")")
  done
  VM_BRIDGES="${bridges[*]}"
  VM_TAPS="${taps[*]}"
  VM_MACS="${macs[*]}"

  ROS_WAN_IFACE="ether1"
  if [ "$count" -ge 2 ]; then
    ROS_LAN_PORTS=""
    for ((i=2; i<=count; i++)); do
      ROS_LAN_PORTS="${ROS_LAN_PORTS:+$ROS_LAN_PORTS }ether$i"
    done
  else
    ROS_LAN_PORTS=""
  fi

  confirm_host_iface_takeover
}

choose_host_net_mode(){
  local choice current
  current="${HOST_NET_MODE:-networkd}"
  printf '\n%s\n' "宿主机网口持久化配置模式:"
  printf '%s\n' "  1. runtime       只在 routeros-chr 服务运行时接管，重启后不保留"
  printf '%s\n' "  2. networkd      写入 systemd-networkd bridge/port 配置"
  printf '%s\n' "  3. nm-unmanaged  只告诉 NetworkManager 不管理这些物理口"
  printf '%s\n' "  4. both          networkd 配置 + NetworkManager unmanaged"
  case "$current" in
    runtime) choice="1" ;;
    nm-unmanaged) choice="3" ;;
    both) choice="4" ;;
    *) choice="2" ;;
  esac
  choice="$(read_default "请选择模式" "$choice")"
  case "$choice" in
    1|runtime) HOST_NET_MODE="runtime"; PERSIST_HOST_NET="0" ;;
    2|networkd) HOST_NET_MODE="networkd"; PERSIST_HOST_NET="1" ;;
    3|nm-unmanaged) HOST_NET_MODE="nm-unmanaged"; PERSIST_HOST_NET="1" ;;
    4|both) HOST_NET_MODE="both"; PERSIST_HOST_NET="1" ;;
    *)
      warn "选择无效，使用当前模式: $current"
      HOST_NET_MODE="$current"
      case "$HOST_NET_MODE" in runtime) PERSIST_HOST_NET="0" ;; *) PERSIST_HOST_NET="1" ;; esac
      ;;
  esac
}

resize_disk_image(){
  local size
  if ! has_cmd qemu-img; then
    warn "缺少 qemu-img，跳过硬盘调整。"
    return 0
  fi
  printf '\n'
  ls -lh "$IMAGE_PATH" 2>/dev/null || true
  if [ "${DISK_FORMAT:-raw}" != "raw" ]; then
    qemu_img_format_probe "$IMAGE_PATH" 3 >/dev/null || warn "镜像格式探测超时或失败，已跳过详细信息显示。"
  fi
  size="$(read_default "如需扩容请输入目标大小，例如 2G、4G；直接回车跳过" "")"
  [ -z "$size" ] && return 0
  if ! is_size_value "$size"; then
    err "大小格式无效，请使用 2G、4096M 这类格式。"
    return 1
  fi
  warn "只建议扩容，不建议缩小镜像。RouterOS 内部文件系统扩展由 RouterOS 自己处理。"
  if confirm "确认执行 qemu-img resize $IMAGE_PATH $size" "n"; then
    qemu-img resize "$IMAGE_PATH" "$size" && ok "镜像大小已调整。"
  fi
}

